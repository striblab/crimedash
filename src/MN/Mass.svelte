  <h3>Mass Shootings</h3>

  <h4 class="source">Data sources: Gun Violence Archive</h4>
  
  <div class="def"><p>The Gun Violence Archive defines a mass shooting as "a minimum of four victims shot, either injured or killed, not including any shooter who may also have been killed or injured in the incident."</p></div>
  
  Since 2014, Minnesota has seen <strong>78</strong> mass shooting incidents, killing <strong>65</strong> victims and <strong>7</strong> suspects. About <strong>340</strong> victims and <strong>7</strong> were injured.

  In that timespan, <strong>53</strong> incidents occured in Minneapolis, <strong>10</strong> in St. Paul, <strong>2</strong> in Duluth, <strong>2</strong> in Burnsville and the rest in other cities.

